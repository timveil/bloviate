package io.bloviate.gen;

import io.bloviate.util.RandomGenerators;
import io.bloviate.util.SeededRandomUtils;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.SQLException;
import java.util.random.RandomGenerator;

/**
 * Abstract base class providing common functionality for data generators.
 * 
 * <p>This class implements common {@link DataGenerator} methods and provides
 * a foundation for specific data type generators. It handles:
 * <ul>
 *   <li>Random seed management for reproducible data generation</li>
 *   <li>Standard JDBC PreparedStatement parameter setting</li>
 *   <li>String representation generation</li>
 *   <li>Coordination between generation and JDBC operations</li>
 * </ul>
 * 
 * <p>Concrete subclasses need only implement the {@link #generate()} method
 * to provide type-specific random value generation. They may optionally
 * override {@link #set(Connection, PreparedStatement, int, Object)} (declared as {@code set(..., T)}) and
 *  for database-specific handling.
 * 
 * @param <T> the Java type of values generated by this generator
 * 
 * @author Tim Veil
 * @see DataGenerator
 */
public abstract class AbstractDataGenerator<T> implements DataGenerator<T> {

    /** The random source backing all value generation; swapped out by {@link #reseed(long)}. */
    protected RandomGenerator random;

    /**
     * A reusable {@link SeededRandomUtils} view over {@link #random}, created once per
     * generator so subclasses don't allocate a fresh wrapper on every {@code generate()}
     * call. It is rebuilt by {@link #reseed(long)} so it always wraps the current
     * {@link #random}.
     */
    protected SeededRandomUtils randomUtils;

    /**
     * Initializes the generator with the given random source and a {@link SeededRandomUtils} view over it.
     *
     * @param random the random source backing all value generation
     */
    public AbstractDataGenerator(RandomGenerator random) {
        this.random = random;
        this.randomUtils = new SeededRandomUtils(random);
    }

    /**
     * Resets only the random source, replacing {@link #random} (and its {@link #randomUtils}
     * view) with a freshly seeded {@link RandomGenerator}. Any counter or sequence state held by
     * a subclass is intentionally left untouched, mirroring the legacy {@code Random.setSeed}
     * behavior the engine relied on for foreign-key wraparound &mdash; only the random draws
     * replay, not the generator's own progression.
     *
     * @param seed the seed for the replacement generator
     */
    @Override
    public void reseed(long seed) {
        this.random = RandomGenerators.create(seed);
        this.randomUtils = new SeededRandomUtils(random);
    }

    @Override
    public final void generateAndSet(Connection connection, PreparedStatement statement, int parameterIndex) throws SQLException {
        T value = generate();
        set(connection, statement, parameterIndex, value);
    }

    @Override
    public void set(Connection connection, PreparedStatement statement, int parameterIndex, T value) throws SQLException {
        statement.setObject(parameterIndex, value);
    }

    @Override
    public String generateAsString() {
        T value = generate();

        if (value != null) {
            return value.toString();
        }

        return null;
    }

}
